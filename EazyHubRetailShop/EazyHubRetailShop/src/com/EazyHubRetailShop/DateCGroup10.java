package com.EazyHubRetailShop;

public class DateCGroup10 {
    private short orderDay, orderMonth, orderYear;

    //Creating Setters and Getters for Order Day
    public void setOrderDay(short orderDay){
        this.orderDay = orderDay;
    }
    public short getOrderDay(){
        return orderDay;
    }

    //Creating Setters and Getters for Order Month
    public void setOrderMonth(short orderMonth){
        this.orderMonth = orderMonth;
    }
    public short getOrderMonth(){
        return orderMonth;
    }

    //Creating Setters and Getters for Order Year
    public void setOrderYear(short orderYear){
        this.orderYear = orderYear;
    }
    public short getOrderYear(){
        return orderYear;
    }
}
